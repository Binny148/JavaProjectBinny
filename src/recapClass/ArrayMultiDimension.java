package recapClass;

public class ArrayMultiDimension {
    public static void main(String[] args) {
        int [][] numbers = {
                {44,62,12,51,33}, // row 0
                {33,66,54,12,25}, // row1
                {65,78,56,42,22}, // row2
                {14,12,15,16,14}, // row3

        };
        System.out.println(numbers[0][1]);
        System.out.println(numbers[2][3]);
    }
}
