package Operators;

public class Practice {

    public static void main(String[] args) {


        int book = 10;



        System.out.println(book++); // 10 is printed then increament--> 11
        System.out.println(++book); // 11 increamnet ---> 12, then it prints

        System.out.println(book); // 12

        System.out.println(book--);  // 12is printed, then it decreased --->11
        System.out.println(--book); // 11----> 10 then prints
        System.out.println(book); // 10


    }
}
